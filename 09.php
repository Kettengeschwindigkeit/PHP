<?php
// Вложенные циклы в PHP. Цикл в цикле, рисуем в терминале - прямоугольник, треугольник, трапецию

// ----------------------------------------------------------------------

for ($k = 0; $k < 5; $k++) {
  for ($i = 0; $i < 20; $i++) {
    echo '*';
  }
  echo "\n";
}

echo "\n"; // ----------------------------------------------------------------------

for ($k = 0; $k < 5; $k++) {
  for ($i = 0; $i <= $k; $i++) {
    echo '*';
  }
  echo "\n";
}

echo "\n"; // ----------------------------------------------------------------------

$out = '';

for ($k = 0; $k < 5; $k++) {
  for ($i = 0; $i <= $k; $i++) {
    $out .= '*';
  }
  $out .= "\n";
}

echo $out, "\n";

"\n"; // ----------------------------------------------------------------------

for ($i = 0; $i <= 10; $i++) {
  $t = $i * 7;
  echo "7 * $i = $t"."\n";
}

echo "\n"; // ----------------------------------------------------------------------

for ($i = 0; $i < 5; $i++) {
  for ($k = 1; $k <=10; $k++) {
    if ($i == 0 AND $k < 10) echo '0';
    echo 10 * $i + $k, " ";
  }
  echo "\n";
}

echo "\n"; // ----------------------------------------------------------------------

for ($k = 5; $k >= 0; $k--) {
  for ($i = 0; $i <= 20; $i++) {
    if ($i <= $k + 3 OR $i >= 20 - $k - 3) {
      echo '-';
    } else {
      echo '*';
    }
  }
  echo "\n";
}

echo "\n";
